# Example
for x in ["spam", "eggs", "ham"]:
    print(x, end=" ")
print("\n")

sum = 0
for x in [1, 2, 3, 4]:
    sum += x
print(sum)

prod = 1
for item in [1, 2, 3, 4]:
    prod *= item
print(prod)
print("\n")


